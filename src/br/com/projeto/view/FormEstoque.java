    private void tabelaProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaProdutosMouseClicked
        // TODO add your handling code here:
        idProduto = Integer.parseInt(tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 0).toString());
        txtNome.setText(tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 1).toString());
        txtEstoqueAtual.setText(tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 3).toString());

    }//GEN-LAST:event_tabelaProdutosMouseClicked

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
      try{
        int qtd_atual, qtd;
        qtd_atual = Integer.valueOf(txtEstoqueAtual.getText());
        qtd = Integer.valueOf(txtAdd.getText());     
        qtd_atualizada = qtd_atual + qtd;
        
        ProdutoDAO dao = new ProdutoDAO();
        dao.adicionarEstoque(idProduto, qtd_atualizada);
      }catch(Exception e){
          JOptionPane.showMessageDialog(null, "erro " + e);
      }
    }//GEN-LAST:event_btnAddActionPerformed

