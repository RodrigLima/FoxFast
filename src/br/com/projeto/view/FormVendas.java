    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String nome = txtProdutoP.getText();
        Produto obj = new Produto();
        ProdutoDAO dao = new ProdutoDAO();
        obj = dao.buscarProdutoNome(nome);
        if(obj.getDescricao() != null){
            int quantidade = Integer.valueOf(txtQtdVende.getText());
            int estoque = Integer.valueOf(txtEstoque.getText());
            preco = Double.valueOf(txtPreco.getText());
            qtd = Integer.valueOf(txtQtdVende.getText());
            subtotal = preco * qtd;
            total += subtotal;

            if(estoque >= quantidade){
                txtValorFInal.setText(String.valueOf(total));
                carrinho = (DefaultTableModel) tabelaCarrinho.getModel();
                carrinho.addRow(new Object[]{
                    txtID.getText(),
                    txtProdutoP.getText(),
                    txtQtdVende.getText(),
                    txtPreco.getText(),
                    subtotal
                });

            }
            else{
                JOptionPane.showMessageDialog(null, "Quantidade Indisponível");
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

