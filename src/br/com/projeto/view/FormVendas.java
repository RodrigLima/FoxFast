    private void btnProcurarCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcurarCliActionPerformed
        // TODO add your handling code here:
         String nome = "%"+txtProduto.getText()+"%";
         ProdutoDAO dao = new ProdutoDAO();
         List<Produto>lista = dao.pesquisarProduto(nome);
         DefaultTableModel dados = (DefaultTableModel) tabelaProdutos.getModel();
         dados.setNumRows(0);
         for(Produto p:lista){
             dados.addRow(new Object[]{
                 p.getId(),
                 p.getDescricao(),
                 p.getPreco(),
                 p.getQtd(),
                 p.getFornecedor().getNome()
             });
         }
    }//GEN-LAST:event_btnProcurarCliActionPerformed

    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        // TODO add your handling code here:
        String nome = txtNome.getText();
        String cpf = txtCPF.getText();
        Cliente obj = new Cliente();
        ClienteDAO dao = new ClienteDAO();
        obj = dao.buscarCliente(nome);
        obj = dao.buscarClienteCPF(cpf);
        if(obj.getNome() != null && obj.getCpf() != null){
            FormPagamentos formPagamentos = new FormPagamentos();
            formPagamentos.txtTotal.setText(String.valueOf(total));
            formPagamentos.cliente = obj;
            formPagamentos.carrinho = carrinho;
            formPagamentos.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnFinalizarActionPerformed

